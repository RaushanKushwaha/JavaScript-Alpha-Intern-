// Area of Circle
 function areaOfCircle(r){
const pi = 3.14;
const AreaOfCircle = pi*r*r;
console.log("Area of Circle: "+AreaOfCircle);
}
areaOfCircle(5);

// Area of Square
 function areaOfSquare(a){
const AreaofSquare = 4*a*a;
console.log("Area of Square: "+AreaofSquare);
}
areaOfSquare(4);

